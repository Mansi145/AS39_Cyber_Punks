function accessDarkCookie(cookieName)
{
  var name = cookieName + "=";
  var allCookieArray = document.cookie.split(';');
  for(var i=0; i<allCookieArray.length; i++)
  {
    var temp = allCookieArray[i].trim();
    if (temp.indexOf(name)==0)
    return temp.substring(name.length,temp.length);
 }
return "";
}


function createDarkThemeCookie(e,o){  

	var localeDomain = window.location.hostname;
    var n = localeDomain.indexOf(".");
    var domain = localeDomain.slice(n, localeDomain.length); 

    var expDate = new Date();
    expDate.setMonth(expDate.getMonth() + 1);
    document.cookie = e +"=" + o + ";expires=" + expDate 
                      + ";domain="+domain+";path=/";
	
 }


function handleDarttheme()
{
    var theme = accessDarkCookie("themeOption");
    if (theme!="" && theme!=null)
    {
        if(theme === "0"){
            setTimeout(function(){
                createDarkThemeCookie("themeOption", 1);
                $('body').addClass('nightmode');
            },10);
        }else{
            setTimeout(function(){
                createDarkThemeCookie("themeOption", 0);
                $('body').removeClass('nightmode');
            },10);
        }
    }
    else{
        createDarkThemeCookie("themeOption", 0);
        $('body').removeClass('nightmode');
    }
}


